                    <p>Le recyclage est un procédé de traitement des déchets (industriels ou ménagers) de produits arrivés en fin de vie, qui permet de réintroduire certains de leurs matériaux dans la production de nouveaux produits. Les matériaux recyclables comprennent certains métaux, plastiques et cartons, le verre, les gravats, etc.</p>
                    <p>Un exemple de ce procédé est la fabrication de bouteilles neuves fabriquées avec le verre de bouteilles usagées, même s'il est considérablement moins efficace énergétiquement que le système des consignes.</p>
                    <p>Le recyclage a deux conséquences écologiques majeures :</p>
